package com.example.carrentalapp_rent4u;

public class ModelVehicle {

    String uid , id , name , description , CategoryId , location , contact , rentalFee , url;
    long timestamp;

    public ModelVehicle() {
    }

    public ModelVehicle(String uid, String id, String name, String description, String categoryId, String location, String contact, String rentalFee, String url, long timestamp) {
        this.uid = uid;
        this.id = id;
        this.name = name;
        this.description = description;
        CategoryId = categoryId;
        this.location = location;
        this.contact = contact;
        this.rentalFee = rentalFee;
        this.url = url;
        this.timestamp = timestamp;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getCategoryId() {
        return CategoryId;
    }

    public void setCategoryId(String categoryId) {
        CategoryId = categoryId;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getContact() {
        return contact;
    }

    public void setContact(String contact) {
        this.contact = contact;
    }

    public String getRentalFee() {
        return rentalFee;
    }

    public void setRentalFee(String rentalFee) {
        this.rentalFee = rentalFee;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public long getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(long timestamp) {
        this.timestamp = timestamp;
    }
}
